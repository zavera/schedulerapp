// the following does not work in PhantomJS:
// tab2Link.trigger('click');
// so we replace it with our own click helper function
// http://stackoverflow.com/questions/36691719/jasmine-click-trigger-doesnt-work-on-a
// Need to create a cross browser click() function no .click() in PhantomJS
// NOTE: keep this function in the global scope??
function click(el) {
    //console.log('call click on element:', el);
    var ev = document.createEvent('MouseEvent');
    ev.initMouseEvent(
        'click',
        true /* bubble */, true /* cancelable */,
        window, null,
        0, 0, 0, 0, /* coordinates */
        false, false, false, false, /* modifier keys */
        0 /*left*/, null
    );
    el.dispatchEvent(ev);
}
